          <?php
$usersQueryArtist = $_POST['usersSearchQueryArtist'];
$usersQueryTitle = $_POST['usersSearchQueryTitle'];
$usersSearchType = $_POST['pullDownOptionUserSelectedSearchType'];
$usersFormat = $_POST['pullDownOptionUserSelectedFormat'];
$usersOrderBy = $_POST['pullDownOptionUserSelectedOrder'];
$usersShowAvailableResultsOnly = $_POST['checkboxShowAvailableOnly'];

//Set default values of post vars if needed (used for quick search)
if(!$usersSearchType){$usersSearchType = "Loose Match";}
if(!$usersFormat){$usersFormat = "All";}
if(!$usersOrderBy){$usersOrderBy = "Best Match";}
if(!$usersShowAvailableResultsOnly){$usersShowAvailableResultsOnly = "No";}

if(($usersQueryArtist != "") && ($usersQueryTitle != ""))
{
	echo "<div id=\"search_results_table\" style=\"margin-left:30px;\"><p>Searching for: \"" . $usersQueryTitle . "\" by " . $usersQueryArtist . " using " . $usersSearchType . " in all fields.</p><br/>";
}
else if($usersQueryArtist != "")
{
	echo "<div id=\"search_results_table\" style=\"margin-left:30px;\"><p>Searching for: \"" . $usersQueryArtist . "\" using " . $usersSearchType . " in Artists.</p><br/>";
}
else if($usersQueryTitle != "")
{
	echo "<div id=\"search_results_table\" style=\"margin-left:30px;\"><p>Searching for: \"" . $usersQueryTitle . "\" using " . $usersSearchType . " in Titles.</p><br/>";
}

//Connect to the database server
$dbServer = "[server]";
$myUserName = "[username]";
$myPassword = "[password]";
@mysql_connect($dbServer, $myUserName, $myPassword)
        or die ("Can't validate my user name/pwd on the local mysql server!");

//echo "Connected to database<br/><br/>";

//Connect to Catalog database (db16310_catalogdb)
$myDatabaseName = "[database]" ;
@mysql_select_db($myDatabaseName)
        or die ("Can't use the database $myDatabaseName !");


//Write base SQL statement (based on loose/exact match selection)
if(($usersQueryArtist != "") || ($usersQueryTitle != ""))
{
	if($usersSearchType == "Loose Match")
	{
		if(($usersQueryArtist != "") && ($usersQueryTitle != ""))
		{
			$sqlQuery = "SELECT *, MATCH (artist) AGAINST ('" . $usersQueryArtist . "') + MATCH (title) AGAINST ('" . $usersQueryTitle . "') AS relevance FROM items WHERE (MATCH (artist) AGAINST ('" . $usersQueryArtist . "') OR MATCH (title) AGAINST ('" . $usersQueryTitle . "')";
		}
		else if($usersQueryArtist != "")
		{
			$sqlQuery = "SELECT *, MATCH (artist) AGAINST ('" . $usersQueryArtist . "') AS relevance FROM items WHERE (MATCH (artist) AGAINST ('" . $usersQueryArtist . "')";
		}
		else if($usersQueryTitle != "")
		{
			$sqlQuery = "SELECT *, MATCH (title) AGAINST ('" . $usersQueryTitle . "') AS relevance FROM items WHERE (MATCH (title) AGAINST ('" . $usersQueryTitle . "')";
		}
	}
	else
	{
		if(($usersQueryArtist != "") && ($usersQueryTitle != ""))
		{
			$sqlQuery .= "SELECT * FROM items WHERE (artist LIKE '%" . $usersQueryArtist . "%' OR title LIKE '%" . $usersQueryTitle . "%'";
		}
		else if($usersQueryArtist != "")
		{
			$sqlQuery .= "SELECT * FROM items WHERE (artist LIKE '%" . $usersQueryArtist . "%'";
		}
		else if($usersQueryTitle != "")
		{
			$sqlQuery .= "SELECT * FROM items WHERE (title LIKE '%" . $usersQueryTitle . "%'";
		}
	}
}
else
{
	$sqlQuery .= "SELECT * FROM items WHERE (ID > 0";
}
$sqlQuery .= ")";

//Concatenate Availablility conditional
if($usersShowAvailableResultsOnly == "Yes")
{
	$sqlQuery .= " AND QtyAvail > 0";
}

//Concatenate Format conditional
if($usersFormat != "All")
{
	if($usersFormat == "CD")
	{
		$sqlQuery .= " AND ConfigAbbr = 'CD'";
	}
	else if($usersFormat == "DVD")
	{
		$sqlQuery .= " AND ConfigAbbr = 'DVD' OR ConfigAbbr = 'DVD-A'";
	}
	else if($usersFormat == "Vinyl")
	{
		$sqlQuery .= " AND ConfigAbbr = 'LP'";
	}
	else if($usersFormat == "DVD")
	{
		$sqlQuery .= " AND ConfigAbbr = 'DVD' OR ConfigAbbr = 'DVD-A'";
	}
	else if($usersFormat == "SA-CD")
	{
		$sqlQuery .= " AND ConfigAbbr = 'SACD'";
	}
	else if($usersFormat == "Accessory")
	{
		$sqlQuery .= " AND ConfigAbbr = 'ACC'";
	}
	else if($usersFormat == "Other")
	{
		$sqlQuery .= " AND ConfigAbbr = 'CASS' OR ConfigAbbr = 'VHS'";
	}
}

//Block porn results
$sqlQuery .= "AND Genre != 'ADULT ENTERTAINMENT' AND Genre != 'ADULT-HARDCORE' AND Genre != 'ADULT' AND Genre != 'CULT-EROTICA' AND Genre != 'ANIME-EROTIC' AND Genre2 != 'ADULT ENTERTAINMENT' AND Genre2 != 'ADULT-HARDCORE'";

//Concatenate display order
if($usersOrderBy != "Best Match")
{
	if($usersOrderBy == "Artist A-Z")
	{
		$sqlQuery .= " ORDER BY Artist, Title";
	}
	else if($usersOrderBy == "Title A-Z")
	{
		$sqlQuery .= " ORDER BY Title, Artist";
	}
	else if($usersOrderBy == "Release Date (Newest First)")
	{
		$sqlQuery .= " ORDER BY STR_TO_DATE(StreetDate,'%m/%d/%Y') DESC, Artist, Title";
	}
	else if($usersOrderBy == "Release Date (Oldest First)")
	{
		$sqlQuery .= " ORDER BY STR_TO_DATE(StreetDate,'%m/%d/%Y') ASC, Artist, Title";
	}
	else if($usersOrderBy == "Price (Ascending)")
	{
		$sqlQuery .= " ORDER BY MSLP ASC, Artist, Title";
	}
	else if($usersOrderBy == "Price (Descending)")
	{
		$sqlQuery .= " ORDER BY MSLP DESC, Artist, Title";
	}
}
else if($usersSearchType == "Loose Match")
{
	$sqlQuery .= " ORDER BY relevance DESC";
}

//echo "<br/>SQL Query: " . $sqlQuery . "<br/><br/>";

//Run the query the user typed in the form
$resultFromQuery = mysql_query($sqlQuery);

if($resultFromQuery)
{
	//Get the number of tuples in the result
	$numberOfTuples = mysql_num_rows($resultFromQuery);

	if (!$numberOfTuples) 
	{
//    die('Error - Query failed: ' . mysql_error());
    die('<br/>No results found.<br/>');
	}
	
	echo "<span style=\"color:#F00; font-size:20px; font-style:italic;\">Item availability is based on current stock at distributor. Please call Finders Records at (419) 352-7677 to check in-store availability.<br/><br/>MSLP is provided as a rough estimate of price and may not correspond to in-store price.</span><br/><br/>";
	echo "<p>Found " . $numberOfTuples . " results:</p>";

	//print the tuples as a nice table
	print "<table width=\"650px\">";
	
	
	// id, LblStdNm, CatalogNum, ConfigAbbr, Artist, Title, UPC, MSLP, QtyAvail, StreetDate, CntryOrgn, Genre, Genre2
	
	for($index=0; $index<$numberOfTuples; $index++) {
		print "<th align=\"left\" colspan=\"2\">" . ($index+1) . "</th>";
		
			//get the next tuple
			$row = mysql_fetch_array($resultFromQuery);
	
			//Format the result cell
			if(strlen($row[5]) > 34)
				print "<tr><td><span style=\"color:#fff350; font-size:22px\">" . substr($row[5], 0, 31) . "...</span><br/>";
			else
				print "<tr><td><span style=\"color:#fff350; font-size:22px\">" . $row[5] . "</span><br/>";
			if(strlen($row[4]) > 40)
				print "<span style=\"font-size:20px\">" . substr($row[4], 0, 37) . "...</span><br/>";
			else
				print "<span style=\"font-size:20px\">" . $row[4] . "</span><br/>";
			if($row[3] == "ACC")
				print "<span style=\"font-size:16px\">Accessory</span><br/>";
			else
				print "<span style=\"font-size:16px\">" . $row[3] . "</span><br/>";
			print "<span style=\"font-size:14px\">[ " . $row[2] . " ]</span><br/><br/>";
			print "<span style=\"font-size:16px\">Genre: " . $row[11] . " - " . $row[12] . "</span><br/>";
			print "<span style=\"font-size:16px\">MSLP: \$" . $row[7] . "</span><br/></td>";
			
			print "<td style=\"padding-right:30px; font-size:16px;\">Release Date: " . $row[9] . "<br/>";
			if($row[10] != "")
				print "Country: " . $row[10] . "<br/><br/>";
			else
				print "<br/><br/>";
			print "<span style=\"font-size:16px\">Label: " . $row[1] . "<br/>";
			print "<span style=\"font-size:16px\">UPC: " . $row[6] . "<br/><br/>";
			if($row[8] > 0)	{print "Available at Distributor";}
			else			{print "On Backorder";}
			print "</td></tr><tr><td>&nbsp;</td></tr>";
	}
	
	print "</table></div>";
}
else
{
	echo "\nNo results found.<br/>";
}

